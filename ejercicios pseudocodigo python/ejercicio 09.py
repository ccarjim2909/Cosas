#Suma de tres números
#Descripción: Solicita tres números y muestra su suma.
#Entrada: num1, num2, num3.
#Salida: Suma total.

num1 = float(input("Introduce el primer número que quieras sumar: "))
num2 = float(input("Introduce el segundo número que quieras sumar: "))
num3 = float(input("Introduce el tercer número que quieras sumar: "))

suma_total = num1 + num2 + num3
print("La suma total de estos tres números es", suma_total)
